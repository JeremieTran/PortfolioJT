                        <h2 class="mb-3">Néolithe, c'est quoi ?</h2>
                        <p>S’enraciner dans la réalité des choses pour mieux innover, c’est l’histoire de la création de Néolithe, née de la transmission entre le savoir-faire artisanal d’un ancien tailleur de pierre et l’audace de deux jeunes ingénieurs. Ce couplage de connaissances du minéral et de l’application de technologies industrielles à la pointe est au service d’une innovation éthique : la Fossilisation Accélérée. Inspirée du vivant, en reproduisant la transformation des déchets du crétacé en calcaire, ce procédé vient bouleverser le mode de traitement des déchets non-recyclables qui étaient, jusqu’alors, enfouis ou incinérés.
                        </p>
                        <h2 class="mb-3">3 étapes fondamentales</h2>
                        <h4>1 ère étape</h4>
                        <p> 
                        <font style="color: red;"> Les déchets </font> </br>
                        Parmi les déchets non-recyclables, le Fossilisateur® prend en entrée les déchets industriels banals (DIB) en mélange tels que le plastique, le bois, les textiles, le plâtre ou encore les isolants. Les métaux, quant à eux, sont retirés du procédé, pour être réutilisés au sein de leur propre filière.                            
                        </p>
                        <h4>2 ème étape</h4>
                        <p> 
                        <font style="color: red;"> La fossilisation accélérée </font> </br>
                        Une fois les déchets non-recyclables, non-inertes et non-dangereux réceptionnés, ils sont transformés, via un process sans émission de polluant, en un broyat de déchets (Fossilisat®) à laquelle est ajouté du liant bas-carbone dont la composition est tenue secrète. Ce liant est l’élément essentiel afin de rendre inerte la matière et lui donner une structure minérale cohérente.                            
                        </p>
                        <h4>3 ème étape</h4>
                        <p> 
                        <font style="color: red;"> Les granulats d’Anthropocite® </font> </br>
                        Cette nouvelle structure minérale, nommée Anthropocite®, dispose des mêmes caractéristiques techniques et mécaniques que celles d’un granulat traditionnel, matière première la plus consommée en France avec 450 millions de tonnes par an. Les granulats d’Anthropocite® créés à partir de la Fossilisation Accélérée des déchets sont utilisables dans le secteur du BTP. Par son action, Néolithe valorise la pierre, un matériau qui se réinvente et trouve sa place dans l’industrie moderne.                            
                        </p>
